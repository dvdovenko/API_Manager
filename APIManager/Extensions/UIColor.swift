import UIKit

extension UIColor {
    
    static var teal: UIColor {
        return UIColor(red: 0.37, green: 0.49, blue: 0.54, alpha: 1.0)
    }
    
    static var charcoal: UIColor {
        return UIColor(red: 0.27, green: 0.27, blue: 0.27, alpha: 1.0)
    }
    
    static var lightCharcoal: UIColor {
        return UIColor(red: 0.27, green: 0.27, blue: 0.27, alpha: 0.4)
    }
    
}
